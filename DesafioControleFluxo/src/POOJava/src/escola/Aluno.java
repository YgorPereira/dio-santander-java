package escola;

public class Aluno {
    private String nome;
    private int idade;
    private String sexo;

    // getter
    public String getNome() {
        return nome;
    }
    // setter
    public void setNome(String nome) {
        this.nome = nome;
    }
    // getter
    public int getIdade() {
        return idade;
    }
    // setter
    public void setIdade(int idade) {
        this.idade = idade;
    }
    // getter
    public String getSexo() {
        return sexo;
    }
    // setter
    public void setSexo(String sexo) {
        this.sexo = sexo;
    }
}